# Solicitar al usuario ingresar dos valores



var1 = float(input("Ingrese el primer valor: "))

var2 = float(input("Ingrese el segundo valor: "))



# Realizar las operaciones matemáticas

suma = var1 + var2

resta = var1 - var2

multiplicacion = var1 * var2

division = var1 / var2

division_entera = var1 // var2

modulo = var1 % var2

potencia = var1 ** var2



# Mostrar los resultados de las operaciones



print("\nResultados de las operaciones:")

print(f"Suma: {var1} + {var2} = {suma}")

print(f"Resta: {var1} - {var2} = {resta}")

print(f"Multiplicación: {var1} * {var2} = {multiplicacion}")

print(f"División: {var1} / {var2} = {division}")

print(f"División entera: {var1} // {var2} = {division_entera}")

print(f"Módulo: {var1} % {var2} = {modulo}")


print(f"Potencia: {var1} ** {var2} = {potencia}")